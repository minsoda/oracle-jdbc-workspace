-- 7번 정답
SELECT TO_DATE('99/10/11', 'YYYY/MM/DD') 값1, 
      TO_DATE('49/10/11', 'YYYY/MM/DD') 값2,
      TO_DATE('99/10/11', 'RR/MM/DD')-100 값3, 
      TO_DATE('49/10/11', 'RR/MM/DD') 값4
