-- 8번 정답
SELECT STUDENT_NO, STUDENT_NAME
FROM TB_STUDENT
WHERE EXTRACT(YEAR FROM ENTRANCE_DATE) > 2000;

-- 9번 정답
SELECT ROUND(AVG(POINT), 1) 평점
FROM TB_GRADE
WHERE STUDENT_NO = 'A517178';

-- 10번 정답
SELECT DEPARTMENT_NO 학과번호, COUNT(*) 학생수
FROM TB_STUDENT
GROUP BY DEPARTMENT_NO;

-- 11번 정답
SELECT COUNT(*)
FROM TB_STUDENDT
WHERE COACH_PROFESSOR_NO IS NULL;

-- 12번 정답
SELECT EXTRACT(YEAR FROM TERM_NO) 년도, ROUND(POINT, 1) 년도별 평점
FROM TM_GRADE
GROUP BY EXTRACT(YEAR FROM TERM_NO)
HAVING TERM_NO BETWEEN '2001' AND '2004';

-- 13번 정답
SELECT DEPARTMENT_NO "학과 코드", COUNT(ABSENCE_YN) "휴학생 수"
FROM TB_STUDENT
GROUP BY DEPARTMENT_NO
HAVING ABSENCE_YN = 'Y';

-- 14번 정답
SELECT STUDENT NAME 동일이름, COUNT(*) 동명인 수
FROM TB_STUDENT
WHERE COUNT(DISTINCT (STUDENT_NO)) > 1;

-- 15번 정답
SELECT SUBSTR(TERM_NO, 1, 4) 년도, 
       SUBSTR(TERM_NO, -1, 2) 학기, 
       CASE 
       WHEN SUBSTR(TERM_NO, -1, 2) = '01' THEN POINT
       WHEN 
      ELSE AVG(POINT) 평균
FROM TB_GRADE
GROUP BY SUBSTR(TERM_NO, 1, 4);
